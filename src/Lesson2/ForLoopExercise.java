package Lesson2;

public class ForLoopExercise {
    public static void main(String[] args) {

        int a = 5; // from 5 to 10
        while (a <= 10) {
            System.out.println(a);
            a++;
        }

        a = 15; // from 15 to 10
        while (a <= 20) {
            System.out.println(a);
            a++;
        }
    }
}
